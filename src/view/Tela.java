    private void jButtonEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEnviarActionPerformed
        String nome = jTextFieldNomeUsuario.getText().trim();
        String ip = jTextFieldIPMulticast.getText().trim();
        String portaStr = jTextFieldPorta.getText().trim();
        String msg = jTextFieldMensagem.getText().trim();

        if (msg.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite uma mensagem.");
            return;
        }

        try {
            int porta = Integer.parseInt(portaStr);
            MessageSender.sendMessage(nome, msg, ip, porta);
            jTextFieldMensagem.setText("");
            jTextFieldMensagem.requestFocus();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao enviar mensagem: " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonEnviarActionPerformed

