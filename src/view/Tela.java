    private void jButtonConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConectarActionPerformed
        String nome = jTextFieldNomeUsuario.getText().trim();
        String ip = jTextFieldIPMulticast.getText().trim();
        String portaStr = jTextFieldPorta.getText().trim();

        if (nome.isEmpty() || ip.isEmpty() || portaStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos.");
            return;
        }

        // Validação básica do IP
        if (!ip.matches("^\\d{1,3}(\\.\\d{1,3}){3}$")) {
            JOptionPane.showMessageDialog(this, "IP inválido. Use o formato 230.0.0.0, por exemplo.");
            return;
        }

        // Valida cada parte do IP
        String[] partes = ip.split("\\.");
        for (String parte : partes) {
            try {
                int valor = Integer.parseInt(parte);
                if (valor < 0 || valor > 255) {
                    JOptionPane.showMessageDialog(this, "Cada parte do IP deve estar entre 0 e 255.");
                    return;
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "IP inválido. Use apenas números e pontos.");
                return;
            }
        }

        // Validação da porta
        int porta;
        try {
            porta = Integer.parseInt(portaStr);
            if (porta < 1024 || porta > 65535) {
                JOptionPane.showMessageDialog(this, "A porta deve estar entre 1024 e 65535.");
                return;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Porta inválida. Use apenas números.");
            return;
        }

        try {
            receptor = new MessageReceiver(ip, porta, jTextArea1);
            receptor.start();

            // Desativa os campos
            jTextFieldNomeUsuario.setEnabled(false);
            jTextFieldIPMulticast.setEnabled(false);
            jTextFieldPorta.setEnabled(false);
            jButtonConectar.setEnabled(false);

            // Ativa o botão enviar
            jButtonEnviar.setEnabled(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao conectar: " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonConectarActionPerformed

